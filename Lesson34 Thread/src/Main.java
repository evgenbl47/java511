public class Main {
    public static void main(String[] args) {
        System.out.println("//1");

        Runnable greeting = () -> {
            System.out.println("Hello! I work as a single stream!");
            System.out.println("Name of Stream: " + Thread.currentThread().getName());
        };
        Thread thread = new Thread(greeting);
        thread.start();
        System.out.println("Main stream is ended");

        System.out.println("//2");

        for (int num = 1; num <= 3; num++) {
            final int studentNum = num;
            Thread student = new Thread(() -> {
                System.out.println("Student " + studentNum + " start solve an exercise");
                System.out.println("Student " + studentNum + " solving...");
                try {
                    Thread.sleep(2000);
                } catch (Exception e) {
                    System.out.println("Error happened");;
                }
                System.out.println("Student " + studentNum + " finish solve an exercise");
            });
            student.start();
        }
    }
}

